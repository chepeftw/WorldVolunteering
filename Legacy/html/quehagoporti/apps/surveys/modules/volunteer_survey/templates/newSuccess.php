<b>I. Objetivo</b>
<p>Esta encuesta tiene como objetivo conocer los perfiles de los voluntarios guatemaltecos que
generan un aporte social importante al desarrollo del país, los cuales servirán de insumo
al Informe Nacional de Desarrollo Humano 2011-2012, sobre Juventudes y Desarrollo
Humano en Guatemala. La información relativa a nombre y correo electrónico servirá para un directorio del CVG; 
el resto de la información se usará de forma anónima y confidencial como parte de la encuesta que servirá 
de insumo para el próximo Informe Nacional de Desarrollo Humano 2011/2012. 
Cualquier duda comunicarse al correo <a href="mailto:coordinador@guatevoluntaria.org">coordinador@guatevoluntaria.org</a> o al telefono 2285-8951.
Agradeceremos que sus respuestas sean completas.</p>

